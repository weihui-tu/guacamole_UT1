    private void confirmerAjouterMachineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmerAjouterMachineActionPerformed
        // TODO add your handling code here:
        String idm=this.txtIdMachineA.getText();
        String etatm=(String)this.cmbEtatM.getSelectedItem();
        
        String ids=(String)this.cmbIdSalle.getSelectedItem();
        
        Controleur c=new Controleur();
        if(c.verifierNbMachine(ids)){
            c.ajouterMachine(idm, etatm, ids);
        
            ListMachine lm=new ListMachine(this.lblnomprenomR1.getText());
            lm.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Le nombre de machine est superieur la capacit√© de la salle.");
        }
        
    }//GEN-LAST:event_confirmerAjouterMachineActionPerformed

