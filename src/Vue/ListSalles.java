    public ListSalles() {
        initComponents();
        
        ArrayList<Salle> touslesSalles=new ArrayList<Salle>();
      
        touslesSalles = c.tousLesSalle();
        
        DefaultTableModel model =(DefaultTableModel)tableSalles.getModel();
        Object rowData[]=new Object[touslesSalles.size()];
        for(int i =0;i<touslesSalles.size();i++){
            rowData[0] = touslesSalles.get(i).getNumeroSalle();
            rowData[1] = touslesSalles.get(i).getNbMachineMax();
            rowData[2] = touslesSalles.get(i).getEtatSalle();
            model.addRow(rowData);
        }
    }
    
