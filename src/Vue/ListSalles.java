    private void supprimerSalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerSalleActionPerformed
        // TODO add your handling code here:
        int row = tableSalles.getSelectedRow();
        String ids=(String)tableSalles.getValueAt(row, 0);
        
        int count=c.supprimerSalle(ids);
        if(count==1){
            ListSalles ls=new ListSalles(this.lblName.getText());
            ls.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "echec de supprimer");
        }
        
        
    }//GEN-LAST:event_supprimerSalleActionPerformed

