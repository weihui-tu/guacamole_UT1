    private void fillFormation(){
        Connection conn = null;
        Formation formation = null;
        try {
            conn = ConnectionBD.getConnection();
            ResultSet rs = Controleur.listFormation(conn, new Formation());
            while(rs.next()){
                formation = new Formation();
                formation.setNomF(rs.getString("nomF"));
                this.cbFormationE.addItem(formation.getNomF());
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void fillGroupe(){
        Connection conn = null;
        Groupe groupe = null;
        try {
            conn = ConnectionBD.getConnection();
            ResultSet rs = Controleur.listGroupe(conn, new Groupe());
            while(rs.next()){
                groupe = new Groupe();
                groupe.setNomG(rs.getString("nomG"));
                this.cbGroupeE.addItem(groupe.getNomG());
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    private void confirmerAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmerAjouterActionPerformed
        // TODO add your handling code here:
        String idE = this.txtIdE.getText();
        String nomE = this.txtNomE.getText();
        String prenomE = this.txtPrenomE.getText();
        String mdpE = this.txtMdpE.getText();
        String groupeE = (String) this.cbGroupeE.getSelectedItem();
        String formationE = (String) this.cbFormationE.getSelectedItem();
     
        
        Formation formation = new Formation(formationE);        
        Groupe groupe = new Groupe(groupeE);
        Etudiant etudiant = new Etudiant(idE, nomE, prenomE, mdpE);
        
        Connection conn = null;
      
        try{
            conn = ConnectionBD.getConnection();
            int idG = Controleur.chercherIdGroupeByNomGNomF(conn, groupe, formation);
            System.out.println(idG);
            int n;
            n = Controleur.addCompteEtudiant(conn, etudiant, idG);
            if( n== 1){
                JOptionPane.showMessageDialog(null, "ajouter un compte etudiant succes");
            }else{
                JOptionPane.showMessageDialog(null, "ajouter un compte etudiant echec");
            }
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"ajouter un compte etudiant echec");
            
        }finally{
            try{
                ConnectionBD.closeConn(conn);
            }catch(Exception e){
                e.printStackTrace();
            }
        }
        
        this.txtIdE.setText("");
        this.txtNomE.setText("");
        this.txtPrenomE.setText("");
        this.txtMdpE.setText("");
        this.cbFormationE.setSelectedItem(null);
        this.cbGroupeE.setSelectedItem(null);
               
        
    }//GEN-LAST:event_confirmerAjouterActionPerformed

