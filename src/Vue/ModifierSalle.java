    private void confirmerModifierSalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmerModifierSalleActionPerformed
        // TODO add your handling code here:
        String ids=this.lblIdS.getText();
        Integer nbm=Integer.valueOf(this.txtNbMachaineM.getText());
        String etats=(String)this.cmbEtatS.getSelectedItem();
        Controleur c=new Controleur();
        int count =c.modifierSalle(ids,nbm,etats);
        if(count==1){
            ListSalles l=new ListSalles(this.lblnomprenomR.getText());
            l.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "echec de modifier");
        }
    }//GEN-LAST:event_confirmerModifierSalleActionPerformed

